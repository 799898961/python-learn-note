import matplotlib
import matplotlib.pyplot
import seaborn

# matplotlib库
# 高质量的二维数据可视化功能库
# 提供了超过100种可视化展示效果
# 通过atplotlib.pyplot子库调用各可视化效果
# Python最主要的数据可视化功能库，基于numpy开发
# http://matplotlib.org


# seaborn库：
# 统计类数据可视化功能库
# 提供了一批高层次的统计类数据可视化展示效果
# 主要展示数据间分布、分类和线性关系等内容
# 基于matplotlib开发，支持numpy和pandas
# http://seaborn.pydata.org/


# mayavi库
# 三维科学数据可视化
