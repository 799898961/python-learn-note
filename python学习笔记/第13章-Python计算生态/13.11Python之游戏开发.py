import pygame

# pygame库
# 简单的游戏开发功能库
# 提供了基于SDL的简单游戏开发功能及实现引擎
# 有助于理解游戏对外部输入的响应机制和角色构建和交互机制
# Python游戏入门最主要的第三方库
# http://www.pygame.org


# panda3d库
# 开源、跨平台的3D渲染和游戏开发库
# 一个3D游戏引擎，提供Python和C++两种接口
# 支持很多先进特性：法线贴图、光泽贴图、卡通渲染等
# 由迪士尼和卡尼基梅隆大学开发
# http://www.panda3d.org


# cocos2d库
# 构建2D游戏和图形界面交互式应用的框架
# 提供基于OpenGL的游戏开发图形渲染功能
# 支持GPU加速，采用树形结构分层管理游戏对象类型
# 适用于2D专业级游戏开发
# http://python.cocos2d.org/