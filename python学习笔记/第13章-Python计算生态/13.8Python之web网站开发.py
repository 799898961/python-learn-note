import django
import flask

# django库
# 最流行的web应用框架
# MTV模式：模型(model)、模板(template)、视图(views)
# Python最重要的web应用框架，略微复杂的应用框架
# https://www.djangoproject.com


# pyramid库
# 规模适中的web应用框架
# 提供了简单方便构建web系统的应用框架
# 应用于不大不小、规模适中的应用需求
# 适合快速构建并适度扩展类应用
# Python产品级web应用框架，起步简单可扩展性好
# https://trypyraid.com/


# flask库
# web应用开发微框架
# 提供了最简单构建web系统的应用框架
# 特点是：简单、规模小、快速
# 专业性和规模：django > pyramid > flask
# http://flask.pocoo.org